package patientsmanagement.patientmanagement.patientsmanagementsystem.entity;

import android.graphics.Bitmap;

/**
 * Created by Administrator on 12/7/2016.
 */
public class Doctor {
    String name,address,phone,expertise,chamberday,chambertime,doctorfee,followupfeee;
    Bitmap image;
    public Doctor(String name, Bitmap image, String address, String phone, String expertise, String chamberday, String chambertime, String doctorfee, String followupfeee){

        this.name = name;
        this.image = image;
        this.address = address;
        this.phone = phone;
        this.expertise = expertise;
        this.chamberday = chamberday;
        this.chambertime = chambertime;
        this.doctorfee = doctorfee;
        this.followupfeee = followupfeee;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Bitmap getImage() {
        return image;
    }

    public void setImage(Bitmap image) {
        this.image = image;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getExpertise() {
        return expertise;
    }

    public void setExpertise(String expertise) {
        this.expertise = expertise;
    }

    public String getChamberday() {
        return chamberday;
    }

    public void setChamberday(String chamberday) {
        this.chamberday = chamberday;
    }

    public String getDoctorfee() {
        return doctorfee;
    }

    public void setDoctorfee(String doctorfee) {
        this.doctorfee = doctorfee;
    }

    public String getChambertime() {
        return chambertime;
    }

    public void setChambertime(String chambertime) {
        this.chambertime = chambertime;
    }

    public String getFollowupfeee() {
        return followupfeee;
    }

    public void setFollowupfeee(String followupfeee) {
        this.followupfeee = followupfeee;
    }
}
